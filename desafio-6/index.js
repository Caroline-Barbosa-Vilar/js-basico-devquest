function somar (numero1, numero2) {
  let soma = numero1 + numero2;
  console.log('A soma dos dois números é: ' + soma);
}
somar(1,2);

function subtrair (numero1, numero2) {
  let subtrai = numero1 - numero2;
  console.log('A subtração dos dois números é: ' + subtrai);
}
subtrair(10,5);

function multiplicar (numero1, numero2) {
  let multiplica = numero1 * numero2;
  console.log('A multiplicação dos dois números é: ' + multiplica);
}
multiplicar(5,6);

function dividir (numero1, numero2) {
  let divide = numero1 / numero2;
  console.log('A divisão dos dois números é: ' + divide);
}
dividir(30,3);